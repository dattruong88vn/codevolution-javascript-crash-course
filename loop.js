// for loop
for (let i = 0; i <= 5; i++) {
  console.log(i);
}

// while loop
let i1 = 0;
while (i1 <= 5) {
  console.log(i1);
  i1++;
}

// do...while
let i2 = 0;
do {
  console.log(i2);
  i2++;
} while (i2 <= 5);

// for...of
const arr = [1, 2, 3, 4, 5];
for (let item of arr) {
  console.log(item);
}
